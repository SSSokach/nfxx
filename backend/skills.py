ALL_SKILLS = [
    SKILL_FILE_SUMMARY,
    SKILL_REPORT_GENERATION,
    SKILL_SMART_REPLY,
    SKILL_TODO_MANAGEMENT,
    SKILL_MEETING_MINUTES,
    SKILL_COMMUNICATION_QUERY,
    SKILL_TEXT_POLISH,
    SKILL_EMAIL_DRAFTING,
    SKILL_INFO_EXTRACTION,
    SKILL_EMAIL_SUMMARY,
]

# 按名称索引
SKILL_MAP = {s["name"]: s for s in ALL_SKILLS}


def match_skill(user_message: str) -> list[dict]:
    """根据用户消息匹配最相关的 Skill。

    采用关键词 + 正则双重匹配，返回匹配得分排序后的 Skill 列表。
    当多个 Skill 得分相同时，按 priority 字段优先级决断。

    Args:
        user_message: 用户输入的消息文本

    Returns:
        按匹配得分降序排列的 Skill 列表，每项附带 _score 字段
    """
    msg = user_message.strip()
    results = []

    for skill in ALL_SKILLS:
        score = 0

        # 关键词匹配
        for kw in skill["trigger_keywords"]:
            if kw in msg:
                score += 2

        # 正则模式匹配
        for pattern in skill["trigger_patterns"]:
            if re.search(pattern, msg):
                score += 3

        if score > 0:
            results.append({**skill, "_score": score})

    # 先按 score 降序，再按 priority 降序（同分时高优先级胜出）
    results.sort(key=lambda x: (x["_score"], x.get("priority", 0)), reverse=True)
    return results


def get_skill_prompt(skill_name: str) -> str:
    """获取指定 Skill 的专属 system prompt。"""
    skill = SKILL_MAP.get(skill_name)
    if skill:
        return skill.get("system_prompt", "")
    return ""


def get_matched_skill_prompt(user_message: str) -> str:
    """根据用户消息匹配 Skill，返回最匹配 Skill 的专属 prompt。

    如果没有匹配到任何 Skill，返回空字符串（使用通用 prompt）。
    """
    matched = match_skill(user_message)
    if matched:
        return matched[0].get("system_prompt", "")
    return ""


def get_matched_skill(user_message: str) -> dict | None:
    """返回最匹配的 Skill 完整定义，无匹配时返回 None。"""
    matched = match_skill(user_message)
    return matched[0] if matched else None


def get_all_skill_names() -> list[str]:
    """返回所有已注册的 Skill 名称列表。"""
    return [s["name"] for s in ALL_SKILLS]


def get_skill_info(skill_name: str) -> dict | None:
    """获取指定 Skill 的完整信息。"""
    return SKILL_MAP.get(skill_name)
